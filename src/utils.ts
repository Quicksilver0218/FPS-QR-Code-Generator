function crc16(data: Uint8Array) {
  let crc = 0xFFFF; // Initial value for CRC16-CCITT
  const polynomial = 0x1021; // CRC16-CCITT polynomial
  for (let i = 0; i < data.length; i++) {
    crc ^= data[i] << 8; // XOR with the current byte shifted left by 8 bits
    for (let j = 0; j < 8; j++)
      if (crc & 0x8000) // Check if the MSB is set
        crc = (crc << 1) ^ polynomial; // Shift left and XOR with polynomial
      else
        crc <<= 1; // Just shift left
  }
  return crc & 0xFFFF; // Return the 16-bit CRC checksum
}

function utf8Crc16Hex(data: string) {
  return crc16(new TextEncoder().encode(data)).toString(16).toUpperCase().padStart(4, "0");
}

function getDataBlock(type: number, data: string) {
  return type.toString().padStart(2, "0") + data.length.toString().padStart(2, "0") + data;
}

type Content = [number, Content][] | string;

function getCode(blocks: [number, Content][]) {
  let code = "";
  for (const [type, content] of blocks)
    if (typeof content === "string")
      code += getDataBlock(type, content);
    else
      code += getDataBlock(type, getCode(content));
  return code;
}

const RECEIVER_TYPES: { [key: string]: number } = {
  FpsId: 2,
  PhoneNumber: 3,
  EmailAddress: 4,
};

export type ReceiverType = keyof typeof RECEIVER_TYPES;

export type AdditionalData = {
  billNumber?: string;
  mobileNumber?: string;
  storeLabel?: string;
  loyaltyNumber?: string;
  referenceLabel?: string;
  customerLabel?: string;
  terminalLabel?: string;
  purposeOfTransaction?: string;
  additionalCustomerDataRequest?: string;
};

const ADDITIONAL_DATA_CODES: { [key in keyof AdditionalData]: number } = {
  billNumber: 1,
  mobileNumber: 2,
  storeLabel: 3,
  loyaltyNumber: 4,
  referenceLabel: 5,
  customerLabel: 6,
  terminalLabel: 7,
  purposeOfTransaction: 8,
  additionalCustomerDataRequest: 9,
};

export function getFpsCode(receiverType: ReceiverType, receiver: string, amount: string, currency: string, additionalData?: AdditionalData) {
  const blocks: [number, Content][] = [
    [0, "01"], // Payload Format Indicator
    [1, additionalData ? "12" : "11"], // Point of Initiation Method
    [52, "0000"], // Merchant Category Code
    [53, currency],
    [54, amount],
    [58, "HK"], // Country Code
    [59, "NA"], // Merchant Name
    [60, "HK"], // Merchant City
    [26, [ // FPS
      [0, "hk.com.hkicl"], // Globally Unique Identifier
      [RECEIVER_TYPES[receiverType], receiver],
    ]]
  ];
  if (additionalData && Object.values(additionalData).filter(Boolean).length !== 0) {
    const now = new Date();
    const year = now.getFullYear().toString().slice(-2);
    const month = (now.getMonth() + 1).toString().padStart(2, "0");
    const day = now.getDate().toString().padStart(2, "0");
    const hour = now.getHours().toString().padStart(2, "0");
    const minute = now.getMinutes().toString().padStart(2, "0");
    const second = now.getSeconds().toString().padStart(2, "0");
    blocks.push([5, `${year}${month}${day}${hour}${minute}${second}`]);
    const additionalDataBlocks: [number, string][] = [];
    for (const [key, value] of Object.entries(additionalData))
      if (value)
        additionalDataBlocks.push([ADDITIONAL_DATA_CODES[key as keyof AdditionalData]!, value]);
    blocks.push([62, additionalDataBlocks]);
  }
  // CRC
  const code = getCode(blocks) + "6304";
  return code + utf8Crc16Hex(code);
}